// ! to work with a chunk of digital data, you have to find a way to represent it on the machine's memory

/* 
  * Arrays - an ordered list of values, allowing a specific way for storing sequences of values.
  ! in JavaScript, you write arrays using a square bracket "[]" and seperated by commas ","
*/

let listOfNo = [2, 4, 6, 8, 10];

console.log(listOfNo);    // -> (5) (2, 4, 6, 8, 10)

console.log(listOfNo[2]);   // -> 6

console.log(listOfNo[0]);   // -> 2

// ! when counting the array order, as you can see, you start with 0 and follow with 1, 2, 3....